// arithmetic operator
// 2 + 3 = 5
// + is operator
// 2 & 3 are operands

// | operator | description         | example   | result    |
// | -------- | -----------         | -------   | ------    |
// | +        | addition            | 5 + 2     | 7         |
// | -        | subtraction         | 5 - 2     | 3         |
// | *        | multiplication      | 5 * 2     | 10        |
// | /        | division            | 5 / 2     | 2.5       |
// | %        | modulus (remainder) | 5 % 2     | 1         |
// | **       | exponentiation      | 2 ** 3    | 8         |



let a = 10;
let b = 5;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);

