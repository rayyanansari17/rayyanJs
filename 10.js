// Assignment operator

// | Operator | Example  | Same As   |
// |-----------|-----------|-----------|
// | =         | x = 5     | x = 5     |
// | +=        | x += 5    | x = x + 5 |
// | -=        | x -= 5    | x = x - 5 |
// | *=        | x *= 5    | x = x * 5 |
// | /=        | x /= 5    | x = x / 5 |
// | %=        | x %= 5    | x = x % 5 |
// | **=       | x **= 3   | x = x ** 3|


let a = 10

console.log(a = 5);
a += 2;
console.log(a);
console.log(a -= 4);
console.log(a *= 6);
console.log(a /= 2);
console.log(a %= 3);
console.log(a **= 4);