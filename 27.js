// Function with Parameters
// parameters ==> passed as variables
// arguments ==> passed as values

function number (a, b, c){
    console.log(a + b + c);
}

number(10, 20, 30)

function whoAmI (fullname){
    console.log(`I am ${fullname} from CFI`);
}

whoAmI("Rayyan Shaji Ansari")


function details (myname, age, phone, city, college){
    console.log(`I am ${myname}, my age is ${age} \nMy Phone Number is ${phone} \nI live in ${city},and my college is ${college}`);

}

details("Rayyan Ansari", 23, 9573109741, "Hyderabad", "MJ College")